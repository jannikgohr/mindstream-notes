// Generates src-tauri/.cargo/config.toml with absolute search paths to the
// prebuilt libsodium static libs.
//
// Cargo's build-script overrides (`[target.<triple>.<links>]`) don't resolve
// `rustc-link-search` paths relative to the config file or the crate manifest
// — they're passed verbatim to the linker — so the paths must be absolute.
// We can't check absolute paths into git, so we generate this file at install
// and before each tauri build/dev run instead.

import { fileURLToPath } from "node:url";
import { dirname, join, resolve } from "node:path";
import { mkdirSync, writeFileSync, existsSync } from "node:fs";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const srcTauriDir = resolve(scriptDir, "..");
const cargoDir = join(srcTauriDir, ".cargo");
const configPath = join(cargoDir, "config.toml");
const prebuiltRoot = join(srcTauriDir, "prebuilt-binaries", "libsodium");

const targets = [
  "x86_64-linux-android",
  "aarch64-linux-android",
  "armv7-linux-androideabi",
  "i686-linux-android",
];

const toToml = (p) => JSON.stringify(p.split("\\").join("/"));

const sections = targets.map((triple) => {
  const debug = join(prebuiltRoot, triple, "debug");
  const release = join(prebuiltRoot, triple, "release");
  return `[target.${triple}.sodium]
rustc-link-search = [${toToml(debug)}, ${toToml(release)}]
rustc-link-lib = ["static=sodium"]`;
});

const contents = `# AUTO-GENERATED by src-tauri/scripts/setup-android-cargo.mjs — do not edit.
# Run \`pnpm setup-android-cargo\` (or \`pnpm install\`) to regenerate.
#
# Suppresses libsodium-sys's build script and points the linker at the
# prebuilt static libs under src-tauri/prebuilt-binaries/. Paths are absolute
# because cargo doesn't resolve relative paths in build-script overrides.

${sections.join("\n\n")}
`;

mkdirSync(cargoDir, { recursive: true });
writeFileSync(configPath, contents);

if (!existsSync(prebuiltRoot)) {
  console.warn(
    `[setup-android-cargo] warning: ${prebuiltRoot} does not exist — android builds will fail to link.`,
  );
}

console.log(`[setup-android-cargo] wrote ${configPath}`);
